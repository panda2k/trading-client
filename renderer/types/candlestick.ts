type Candlestick = {
    /**
     * Opening price of candle
     */
    open: Number

    /**
     * Closing price of candle
     */
    close: Number

    /**
     * Highest price of candle
     */
    high: Number

    /**
     * Lowest price of candle
     */
    low: Number

    /**
     * Open time of candle
     */
    openTime: Date

    /**
     * Close time of candle
     */
    closeTime: Date

    /**
     * Volume of candle
     */
    volume: Number
}


